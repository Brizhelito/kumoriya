/// Cloudy Cozy radius scale.
///
/// Generous radii reinforce the "cloud pillow" softness.
abstract final class CloudRadius {
  /// 12px — small elements, chips, nav items.
  static const double sm = 12.0;

  /// 16px — medium cards, inputs, buttons (outlined).
  static const double md = 16.0;

  /// 24px — large cards, dialogs, bottom sheets.
  static const double lg = 24.0;

  /// 32px — poster images, large interactive surfaces.
  static const double xl = 32.0;

  /// 999px — pills, badges, circular buttons.
  static const double pill = 999.0;
}
