/// Cloudy Cozy spacing scale.
///
/// All spacing in the design system references these tokens.
/// Never hard-code pixel values in layout code.
abstract final class CloudSpacing {
  /// 4px — icon-to-label gaps, badge internal padding.
  static const double s1 = 4.0;

  /// 8px — row gap, chip internal V-padding.
  static const double s2 = 8.0;

  /// 12px — card internal padding (compact), list item gap.
  static const double s3 = 12.0;

  /// 16px — screen horizontal padding (mobile), button V-padding.
  static const double s4 = 16.0;

  /// 24px — section spacing, large card padding, screen padding (tablet).
  static const double s5 = 24.0;

  /// 32px — screen horizontal padding (desktop), page top padding.
  static const double s6 = 32.0;

  /// 48px — large section spacing.
  static const double s7 = 48.0;

  /// 64px — hero section padding.
  static const double s8 = 64.0;
}
