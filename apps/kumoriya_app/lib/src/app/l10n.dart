String displayTagCategoryLabel(BuildContext context, String category) {
  if (Localizations.localeOf(context).languageCode != 'es') {
    return category;
  }

  final normalized = category.trim().toLowerCase();
  final direct = _categoryValueTranslationsEs[normalized];
  if (direct != null) {
    return direct;
  }

  final parts = normalized.split('-');
  if (parts.length >= 2) {
    final prefix = _categoryPrefixTranslationsEs[parts.first];
    final suffix =
        _categoryValueTranslationsEs[parts.sublist(1).join('-')] ??
        _categoryValueTranslationsEs[parts.sublist(1).join(' ')];
    if (prefix != null && suffix != null) {
      return '$prefix - $suffix';
    }
  }

  return category;
}

String displayTagLabel(BuildContext context, String tag) {
  if (Localizations.localeOf(context).languageCode != 'es') {
    return tag;
  }

  return _tagTranslationsEs[tag] ?? _translateTagByWords(tag);
}

String _translateTagByWords(String tag) {
  final lower = tag.toLowerCase();
  if (_tagWordsKeepAsIs.contains(lower)) {
    return tag;
  }

  final tokens = tag.split(' ');
  final translated = tokens
      .map((token) {
        final normalized = token.toLowerCase();
        if (_tagWordsKeepAsIs.contains(normalized)) {
          return token;
        }

        if (normalized.contains('-')) {
          final parts = normalized.split('-');
          final mappedParts = parts
              .map((part) => _tagWordTranslationsEs[part] ?? part)
              .toList(growable: false);
          return mappedParts.join('-');
        }

        return _tagWordTranslationsEs[normalized] ?? token;
      })
      .toList(growable: false);

  return translated.join(' ');
}
